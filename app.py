import streamlit as st
import pandas as pd
import joblib
st.sidebar.title("Navigation")

st.sidebar.info("""
Biomedical Engineering Project

Machine Learning Model:
Random Forest

Dataset:
1000 Synthetic Patient Records

Developer:
VISALATCHI
""")
# Load trained model
model = joblib.load("Best_KRAS_Model.pkl")

# Load label encoders
encoders = joblib.load("label_encoders.pkl")
st.write("Gender classes:", encoders["Gender"].classes_)
st.write("Smoking classes:", encoders["Smoking"].classes_)
st.write("KRAS classes:", encoders["KRAS_Mutation"].classes_)

# Title
st.title("Colorectal Cancer Prediction System")
st.progress(100)
st.markdown("""
### Integrative Machine Learning Pipeline for Predicting Colorectal Cancer Susceptibility via KRAS Mutation Signatures
""")

st.write("Enter the patient details below.")
# Patient ID
patient_id = st.number_input("Patient ID", min_value=1, value=1001)

# Age
age = st.number_input("Age", min_value=1, max_value=120, value=50)

# Gender
gender = st.selectbox("Gender", ["Male", "Female"])

# BMI
bmi = st.number_input("BMI", min_value=10.0, max_value=50.0, value=25.0)

# Smoking
smoking = st.selectbox("Smoking", ["Yes", "No"])

# Alcohol
alcohol = st.selectbox("Alcohol", ["Yes", "No"])

# Family History
family_history = st.selectbox("Family History", ["Yes", "No"])

# Diabetes
diabetes = st.selectbox("Diabetes", ["Yes", "No"])

# Physical Activity
physical_activity = st.selectbox(
    "Physical Activity",
    ["Low", "Moderate", "High"]
)

# Diet Score
diet_score = st.slider("Diet Score", 1, 10, 5)

# KRAS Mutation
kras = st.selectbox(
    "KRAS Mutation",
    ["Mutated", "Wild-Type"]
)

# Tumor Size
tumor_size = st.number_input(
    "Tumor Size (cm)",
    min_value=0.0,
    value=0.0
)

# Cancer Stage
stage = st.selectbox(
    "Cancer Stage",
    ["0", "I", "II", "III", "IV"]
)

# CEA Level
cea = st.number_input(
    "CEA Level",
    min_value=0.0,
    value=2.5
)# Predict Button
if st.button("Predict"):

    # Convert text inputs to numbers using saved label encoders
    gender = encoders["Gender"].transform([gender])[0]
    smoking = encoders["Smoking"].transform([smoking])[0]
    alcohol = encoders["Alcohol"].transform([alcohol])[0]
    family_history = encoders["Family_History"].transform([family_history])[0]
    diabetes = encoders["Diabetes"].transform([diabetes])[0]
    physical_activity = encoders["Physical_Activity"].transform([physical_activity])[0]
    kras = encoders["KRAS_Mutation"].transform([kras])[0]
    stage = encoders["Cancer_Stage"].transform([stage])[0]

    # Create input dataframe
    input_data = pd.DataFrame({
        "Patient_ID": [patient_id],
        "Age": [age],
        "Gender": [gender],
        "BMI": [bmi],
        "Smoking": [smoking],
        "Alcohol": [alcohol],
        "Family_History": [family_history],
        "Diabetes": [diabetes],
        "Physical_Activity": [physical_activity],
        "Diet_Score": [diet_score],
        "KRAS_Mutation": [kras],
        "Tumor_Size_cm": [tumor_size],
        "Cancer_Stage": [stage],
        "CEA_Level": [cea]
    })

    # Make prediction
    prediction = model.predict(input_data)
    probability = model.predict_proba(input_data)
    confidence = max(probability[0]) * 100

    # Convert prediction back to text
    result = encoders["Diagnosis"].inverse_transform(prediction)
st.subheader("Prediction Result")
st.subheader("Patient Details")

st.write(f"Age : {age}")

st.write(f"Gender : {gender}")

st.write(f"Smoking : {smoking}")

st.write(f"KRAS Mutation : {kras}")

st.write(f"CEA Level : {cea}")

if result[0] == "Positive":
    st.error("⚠️ High Susceptibility to Colorectal Cancer")
else:
    st.success("✅ Low Susceptibility to Colorectal Cancer")
    st.balloons()

st.info(f"Prediction Confidence: {confidence:.2f}%")
st.warning(
    "This prediction is generated by a machine learning model "
    "for educational purposes only and should not be used as a medical diagnosis."
)
